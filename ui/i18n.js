// Get user's system language (or fallback to 'pt')
function getSystemLanguage() {
  const navLang = navigator.language.split("-")[0]; // e.g., "pt" from "pt-BR"
  return translations[navLang] ? navLang : "en";
}

// Translate a key with current language
function t(key, lang = null) {
  const targetLang = lang || currentLanguage;
  return (translations[targetLang] && translations[targetLang][key]) || key;
}

// Get all languages available in translations
function getAvailableLanguages() {
  return Object.keys(translations);
}

